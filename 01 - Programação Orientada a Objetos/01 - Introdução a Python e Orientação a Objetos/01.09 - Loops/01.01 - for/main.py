# Loop for

for i in range(6):
    print(i)

# OBS: A função range() pode ser representada de três formas diferentes:
# range(stop_value) : Considera o ponto inicial como zero.
# range(start_value, strop_value) : Gera a sequência com base no valor inicial e final.
# range(start_value, stop_value, step_size) : Gera a sequência incrementando o valor inicial usando o tamanho do passo
#     até atingir o valor final.