# Indexação e Substrings

frase = "Python é muito divertido!"

# A indexação começa começa com 0
print(frase[3])

# Use -1 para pegar a última letra
print(frase[-1])

# Use -2 para pegar a penúltima letra
print(frase[-2])

# string[start:end] - end não é incluso
print(frase[:4])
print(frase[10:])
print(frase[5:10])
print(frase[:])